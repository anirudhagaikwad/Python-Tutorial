#Conversion between data types
set([1,2,3]) # List to Set
#{1, 2, 3}
tuple({5,6,7})# Set to Tuple
#(5, 6, 7)
list('hello') # String to List
#['h', 'e', 'l', 'l', 'o']
dict([[1,2],[3,4]]) # List to Dictionary
#{1: 2, 3: 4}
dict([(3,26),(4,44)]) # Tuple to Dictionary
#{3: 26, 4: 44}
int('12') #String to integer
#12
float(12) #Integer to Float
int(2.5) #Float to integer
